def check(n):
      
    # if the number is positive
    if n > 0:
        print("Positive")
          
    # if the number is negative
    elif n < 0:
        print("Negative")
          
    # if the number is equal to
    # zero
    else:
        print("Equal to zero")
          
# Driver Code
check(5)
check(0)
check(-5)